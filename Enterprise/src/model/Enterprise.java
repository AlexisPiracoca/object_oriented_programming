package model;

/**
 *
 * @author andresforero
 */
public class Enterprise {
    //Attributes
    private int id;
    private String name;
    
    //Constructors
    public Enterprise() {
    }
    
    public Enterprise(int id, String name) {
        this.id = id;
        this.name = name;
    }
    
    public void setId(int id){
        this.id = id;
    }
    
    public int getId(){
        return this.id;
    }
    
    public String getName(){
        return this.name;
    }
    
    public void setName(String name){
        this.name = name;
    }
    
}
